package com.upskill.java1;

public class DataTypes {

	public static void main(String[] args) {
		
		System.out.println("Hello world ,This is Nazmin");
		
		int a = 859364569;
		int b = 25869;
		int c = a+b;
		System.out.println(c);
		
		String state = "NewYork";
		String city = "Bronx";
		System.out.println(state);
		System.out.println(city);
		
		boolean isRain = false;
		System.out.println(isRain);
		
		//example of all primitive Datatypes
		
		int e = 123456789; // int can go more 5 or less than 5
		short f = 12345; //short can not go more than 5 , it's has to be 5 digit or less than 5
		long g = 1234567891234567923L; //long not to be more than 16 digit and at the end has to be 'L' letter
		byte h = 123; //byte not to be more than 3 digit
		float i = 3.45F; //float to be decimal number also it's has to be 'F' letter at the end
		double j = 3.236;// double is to be decimal number.
		char k = 'T'; //char will be single letter with single quotation
		boolean l = true;    // boolean is define true or false
		boolean m = false; 
		
	}

}
